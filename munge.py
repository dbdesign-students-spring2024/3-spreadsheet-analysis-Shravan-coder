def modification_function(value):
    # Find the positions of the first and second occurrences of "-"
    first_dash_index = value.find("-")
    second_dash_index = value.find("-", first_dash_index + 1)
    
    # If two dashes are found, return the substring after the character before the second dash; otherwise, return the original value
    return value[second_dash_index + 2:] if first_dash_index != -1 and second_dash_index != -1 else value


